public class nestedLoop {
    public static void main(String[] args) {
        // nested loop = consist of an outer loop and or more inner loops
        // task: to print number 10 to 19

        for(int i = 1; i <= 1; i++) {
            for (int j = 0; j <= 9; j++){
                System.out.println(i + " " + j);
            }
        }
    }
}
